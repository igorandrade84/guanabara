# Calcule média de duas notas de um aluno da escola.
# prestar atenção à ordem de precedência. Os parenteses definem na expressão quem 
# será lido primeiro em conjunto

n1 = float(input('DIGITE A PRIMEIRA NOTA: '))
n2 = float(input('DIGITE A SEGUNDA NOTA: '))
M = (n1 + n2) / 2
print(f'A MÈDIA ENTRE {n1} e {n2} É DE : {M}')